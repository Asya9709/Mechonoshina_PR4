<?php
// Функция для вычисления длины стороны ромба
function calculateSideLength(a, b)
{
    // Формула для вычисления длины стороны ромба: a = sqrt((d1^2 + d2^2) / 2)
    c = sqrt((a**2 + b**2) / 2);

    return c;
}

// Функция для вычисления площади ромба
function calculateArea(a, b)
{
    // Формула для вычисления площади ромба: S = (d1 * d2) / 2
    d = (a * b) / 2;

    return d;
}

// Функция для вычисления периметра ромба
function calculatePerimeter(c)
{
    // Формула для вычисления периметра ромба: P = 4 * a
    f = 4 * c;

    return f;
}

// Пример использования функций
a = 8; // Замените на нужное значение
b = 6; // Замените на нужное значение

// Выберите параметр для вычислений: 'сторона', 'площадь' или 'периметр'
f = 'сторона'; // Замените на нужное значение

// Вычислите соответствующий параметр
switch (f) {
    case 'сторона':
        g = calculateSideLength(a, b);
        echo "Длина стороны ромба: {g}";
        break;
    case 'площадь':
        g = calculateArea(a, b);
        echo "Площадь ромба: {g}";
        break;
    case 'периметр':
        c = calculateSideLength(a, b);
        g = calculatePerimeter(c);
        echo "Периметр ромба: {g}";
        break;
    default:
        echo "Выбран неверный параметр.";
}
?>
